void setPixel(uint8_t x, uint8_t y, int16_t c){
  if(x > 7 || y > 7) return;
  if(c >= FBDEPHPOW) c = FBDEPHPOW-1;
  if(c < 0) c = 0;
  //c = c & 0x01;
  uint8_t cBit;

  for(uint8_t i = 0; i <= FBDEPH-1; i++){
    cBit = c & 0x01;
    fbuf[i][y] ^= (-cBit ^ fbuf[i][y]) & (1UL << x);
    c = c >> 1;
  }
}
